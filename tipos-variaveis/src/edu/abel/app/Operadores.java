package edu.abel.app;

public class Operadores {
    public static void main(String[] args) {
        int numero = 5;

        numero = -numero;

        System.out.println(- numero);
        System.out.println(numero);

        numero = -numero;
        System.out.println(numero);

        numero++;
        System.out.println(numero);

        numero--;
        System.out.println(numero);
    }
}
